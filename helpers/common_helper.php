function get_msg($lang='us', $msg) {
    if (!isset($lang)) {
        $lang = 'us';
    }

    $lang = get_lang($lang);

    try
    {
        if (isset($lang[$msg])) {
            return $lang[$msg];
        } else {
            return $msg;
        }
    }
    catch(Exception $e)
    {
        return $msg;
    }
}

 function paging($link, $total, $page, $size,$getValue) {

		if($total == 0) return;

		$total_page = ceil($total / $size);
		$temp = $page % 10;

		if($temp == 0) {
			$a = 10 - 1;
			$b = $temp;
		}
		else {
			$a = $temp - 1;
			$b = 10 - $temp;
		}

		$start = $page - $a;
		$end = $page + $b;
		
		//이전페이지
		if($page > 10) {
			$back_page = $start - 1;
			echo "<li><a href='$link?page=$back_page$getValue'><</a></li>";
		}

		//페이지 출력
		for($i = $start; $i <= $end; $i++) {
			if($i > $total_page) break;

			if($page == $i) echo "<li class=\"active\"><a href='#'>$i</a></li>";
			else echo "<li><a href='$link?page=$i$getValue' >$i</a></li>";

		}


		//다음페이지
		if($end < $total_page) {
			$next_page = $end + 1;
			echo "<li><a href='$link?page=$next_page$getValue'>></a></li>";
		}

	
	}
	
