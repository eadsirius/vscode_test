	if (!$url)
		echo "self.close()";
	echo "</script>";
	if ($url)
		goto_url($url);
	exit;
}


// 해당 url로 이동
function goto_url($url) {
	$temp = parse_url($url);
	if (empty($temp['host'])) {
		$CI =& get_instance();
		$url = ($temp['path'] != '/') ? '/'.$url : $CI->config->item('base_url');
	}
	echo "<script type='text/javascript'> location.replace('".$url."'); </script>";
	exit;
}

