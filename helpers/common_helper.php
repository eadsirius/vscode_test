// 메세지창
function alert($msg='', $url='') {
	$CI =& get_instance();

	if (!$msg) $msg = '올바른 방법으로 이용해 주십시오.';

	echo "<meta http-equiv=\"content-type\" content=\"text/html; charset=".$CI->config->item('charset')."\">";
	echo "<script type='text/javascript'>alert('".$msg."');";
	if (!$url)
		echo "history.go(-1);";
	echo "</script>";
	if ($url)
		goto_url($url);
	exit;
}


// 메시지창 후 닫기
